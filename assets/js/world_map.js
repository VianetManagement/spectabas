// --- Coordinate conversion ---

/**
 * Convert lon/lat to canvas pixel coordinates.
 * Equirectangular projection: lon maps linearly to x, lat maps (inverted) to y.
 * @param {number} lon - Longitude (-180 to 180)
 * @param {number} lat - Latitude (-90 to 90)
 * @param {object} area - Drawing area {left, top, width, height}
 * @returns {[number, number]} [x, y] pixel coordinates
 */
export function lonLatToXY(lon, lat, area) {
  const x = area.left + ((lon + 180) / 360) * area.width
  const y = area.top + ((90 - lat) / 180) * area.height
  return [x, y]
}

/**
 * Find which country contains the given pixel coordinates.
 * Uses ray-casting point-in-polygon test.
 * @param {number} px - Pixel x
 * @param {number} py - Pixel y
 * @param {object} area - Drawing area {left, top, width, height}
 * @returns {object|null} Country object {i, n, p} or null
 */
export function countryAtPoint(px, py, area) {
  // Convert pixel back to lon/lat
  const lon = ((px - area.left) / area.width) * 360 - 180
  const lat = 90 - ((py - area.top) / area.height) * 180

  for (const country of WORLD_MAP) {
    for (const ring of country.p) {
      if (pointInPolygon(lon, lat, ring)) return country
    }
  }
  return null
}

function pointInPolygon(x, y, ring) {
  let inside = false
  for (let i = 0, j = ring.length - 1; i < ring.length; j = i++) {
    const xi = ring[i][0], yi = ring[i][1]
    const xj = ring[j][0], yj = ring[j][1]
    if ((yi > y) !== (yj > y) && x < ((xj - xi) * (y - yi)) / (yj - yi) + xi) {
      inside = !inside
    }
  }
  return inside
}

// --- Chart.js Plugin ---

/**
 * Chart.js plugin that draws the world map as a background layer.
 *
 * Register it: Chart.register(worldMapPlugin)
 *
 * Plugin options (set via options.plugins.worldMap):
 *   fillColor:   Country fill color (default: "#e2e8f0")
 *   strokeColor: Country border color (default: "#cbd5e1")
 *   lineWidth:   Border width (default: 0.5)
 *   highlight:   Map of country name -> color for highlighting specific countries
 *                e.g. { "United States of America": "#6366f1", "Brazil": "#10b981" }
 *
 * Example:
 *   new Chart(canvas, {
 *     type: "scatter",  // or any type; the map draws in beforeDraw
 *     plugins: [worldMapPlugin],
 *     options: {
 *       plugins: {
 *         worldMap: {
 *           fillColor: "#f1f5f9",
 *           strokeColor: "#94a3b8",
 *           highlight: { "Brazil": "rgba(99,102,241,0.5)" }
 *         }
 *       }
 *     }
 *   })
 */
export const worldMapPlugin = {
  id: "worldMap",

  beforeDraw(chart, _args, options) {
    const { ctx, chartArea } = chart
    if (!chartArea) return

    const area = {
      left: chartArea.left,
      top: chartArea.top,
      width: chartArea.right - chartArea.left,
      height: chartArea.bottom - chartArea.top,
    }

    const fillColor = options.fillColor || "#e2e8f0"
    const strokeColor = options.strokeColor || "#cbd5e1"
    const lineWidth = options.lineWidth ?? 0.5
    const highlight = options.highlight || {}

    ctx.save()

    for (const country of WORLD_MAP) {
      const countryFill = highlight[country.n] || fillColor

      for (const ring of country.p) {
        ctx.beginPath()
        const [x0, y0] = lonLatToXY(ring[0][0], ring[0][1], area)
        ctx.moveTo(x0, y0)
        for (let k = 1; k < ring.length; k++) {
          const [x, y] = lonLatToXY(ring[k][0], ring[k][1], area)
          ctx.lineTo(x, y)
        }
        ctx.closePath()

        ctx.fillStyle = countryFill
        ctx.fill()

        if (lineWidth > 0) {
          ctx.strokeStyle = strokeColor
          ctx.lineWidth = lineWidth
          ctx.stroke()
        }
      }
    }

    ctx.restore()
  },
}

